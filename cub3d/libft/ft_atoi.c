#include "libft.h"

int		ft_atoi(const char *str)
{
	int	sign;
	long	result;
	int	i;

	i = 0;
	sign = 1;
	result = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + *str - '0';
		str++;
		i++;
	}
	if (i > 20)
		return ((sign == 1) ? -1 : 0);
	return (sign * result);
}
